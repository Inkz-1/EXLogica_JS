function toggleExerciciosFaccat() {
    const elemento = document.getElementById('exerciciosFaccat');
    
    if (elemento.style.display === 'none' || elemento.style.display === '') {
      elemento.style.display = 'block';
    } else {
      elemento.style.display = 'none';
    }
  }
